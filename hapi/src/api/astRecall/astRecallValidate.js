'use strict';
const model = require('./astRecallSchema');

function astRecallValidate() {}
astRecallValidate.prototype = (() => {

    return {
        findPost: {
            payload: (() => new model().schemaFindPost() )()
        },
        insert: {
            payload: (() => {
                const schema = new model().schema;
                delete schema.HIID;
                return schema;
            })()
        },
        update: {
            payload: (() => new model().schema )()
        },
        delete: {
            params: (() => {
                const schema = new model().schema;
                return {
                    rcID: schema.rcID
                };
            })()
        },
        recall: (() => {
            return {
                payload: (() => new model().schemaRecall() )()
            };
        })()
    };
})();

const validate = new astRecallValidate();
module.exports = validate;

