'use strict';
const Joi = require('joi');
const model = require('./crmTagSchema');

function crmTagValidate() {}
crmTagValidate.prototype = (() => {

    return {
        findPost: {
            payload: (() => new model().schemaFindPost() )()
        },
        insert: {
            payload: (() => {
                const schema = new model().schema;
                delete schema.HIID;
                return schema;
            })()
        },
        update: {
            payload: (() => new model().schema )()
        },
        delete: {
            params: {
                tagID: Joi.number().integer().required()
            }
        }
    };
})();

const validate = new crmTagValidate();
module.exports = validate;

