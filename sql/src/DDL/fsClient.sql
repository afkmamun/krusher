CREATE TABLE IF NOT EXISTS `fsClient` (
	`fclID` INT(11) NOT NULL,
	`Aid` INT(11) NOT NULL DEFAULT '0' COMMENT 'ID клиента',
	`sn` VARCHAR(150) NULL DEFAULT NULL,
	`fclName` VARCHAR(200) NULL DEFAULT NULL,
	`clID` INT(11) NULL DEFAULT NULL,
	`isConvert` BIT(1) NOT NULL DEFAULT b'0',
	`ffID` INT(11) NOT NULL,
	`Comments` VARCHAR(1020) NULL DEFAULT NULL,
	`ShortName` VARCHAR(50) NULL DEFAULT NULL,
	`KVED` VARCHAR(7) NULL DEFAULT NULL,
	`KVEDName` VARCHAR(250) NULL DEFAULT NULL,
	`CompanyID` INT(11) NULL DEFAULT NULL,
	`isPerson` BIT(1) NOT NULL DEFAULT b'0',
	`headPost` VARCHAR(200) NULL DEFAULT NULL,
	`headFIO` VARCHAR(50) NULL DEFAULT NULL,
	`headFam` VARCHAR(50) NULL DEFAULT NULL,
	`headIO` VARCHAR(100) NULL DEFAULT NULL,
	`headSex` VARCHAR(10) NULL DEFAULT NULL,
	`isRobocall` BIT(1) NOT NULL DEFAULT b'0',
	`phoneCode` VARCHAR(25) NULL DEFAULT NULL,
	`ActDate` DATE NULL DEFAULT NULL,
	`timeZone` SMALLINT(6) NULL DEFAULT NULL,
	`Operator` VARCHAR(50) NULL DEFAULT NULL,
	`RegCode` INT(11) NULL DEFAULT NULL,
	`TaxCode` VARCHAR(14) NULL DEFAULT NULL,
	`Address` VARCHAR(200) NULL DEFAULT NULL,
	`Region` VARCHAR(100) NULL DEFAULT NULL,
	`RegionDesc` VARCHAR(200) NULL DEFAULT NULL,
	`langID` INT(11) NULL DEFAULT NULL,
	`curID` INT(11) NULL DEFAULT NULL,
	`sum` DECIMAL(14,2) NULL DEFAULT NULL,
	`ttsText` LONGTEXT NULL DEFAULT NULL,
	`isActive` VARCHAR(200) NOT NULL DEFAULT '0' COMMENT 'Признак активности записи',
	`Created` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT 'Время создания записи',
	`Changed` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT 'Время редактирования записи',
	PRIMARY KEY (`fclID`),
	INDEX `IX_fsClient_sn` (`sn`),
	INDEX `IX_fsClient_clID` (`clID`),
	INDEX `IX_fsClient_ffID` (`ffID`),
	INDEX `IX_fsClient_isConvert` (`isConvert`),
	INDEX `Aid` (`Aid`),
	INDEX `isActive` (`isActive`),
	INDEX `Created` (`Created`)
)
COLLATE='utf8_general_ci'
ENGINE=MyISAM
;
